// ==================== 当前语言 ====================
let currentLang = 'en'; // 默认英文（B2B出口网站）

// ==================== 切换语言 ====================
function switchLanguage(lang) {
  currentLang = lang;
  const t = translations[lang];

  // 更新 HTML lang 属性
  document.documentElement.lang = lang === 'zh' ? 'zh-CN' : 'en';

  // 导航
  setText('[href="#products"]', t.nav.products);
  setText('[href="#advantages"]', t.nav.advantages);
  setText('[href="#about"]', t.nav.about);
  setText('[href="#contact"].nav-cta, [href="#contact"].cta-mobile', t.nav.contact);
  setText('.nav-cta', t.nav.quote);

  // Hero
  setText('.hero-badge', t.hero.badge);
  setText('.hero-tagline', t.hero.tagline);
  
  // Hero 标题（保留 <span> 结构）
  const heroH1 = document.querySelector('.hero h1');
  if (heroH1) {
    heroH1.innerHTML = `${t.hero.title1}<span>${t.hero.title2}</span>${t.hero.title3}`;
  }
  
  setText('.hero p', t.hero.description);
  
  const heroActions = document.querySelectorAll('.hero-actions .btn');
  if (heroActions[0]) heroActions[0].childNodes[0].textContent = t.hero.cta1 + ' ';
  if (heroActions[1]) heroActions[1].textContent = t.hero.cta2;

  // Hero stats
  const statNums = document.querySelectorAll('.hero-stat-num');
  const statLabels = document.querySelectorAll('.hero-stat-label');
  if (statNums[0]) statNums[0].textContent = t.hero.stat1;
  if (statLabels[0]) statLabels[0].textContent = t.hero.stat1Label;
  if (statNums[1]) statNums[1].textContent = t.hero.stat2;
  if (statLabels[1]) statLabels[1].textContent = t.hero.stat2Label;
  if (statNums[2]) statNums[2].textContent = t.hero.stat3;
  if (statLabels[2]) statLabels[2].textContent = t.hero.stat3Label;

  // Categories
  setText('.categories .section-label', t.categories.label);
  setText('.categories .section-title', t.categories.title);
  setText('.categories .section-desc', t.categories.subtitle);

  // Products
  setText('.products-inner .section-label', t.products.label);
  setText('.products-inner .section-title', t.products.title);
  setText('.products-inner .section-desc', t.products.subtitle);
  setPlaceholder('#searchInput', t.products.searchPlaceholder);
  
  // "All Products" 按钮
  const allBtn = document.querySelector('.filter-btn[data-filter="all"]');
  if (allBtn) allBtn.textContent = t.products.allProducts;

  // Advantages
  setText('.advantages .section-label', t.advantages.label);
  setText('.advantages .section-title', t.advantages.title);
  setText('.advantages .section-desc', t.advantages.subtitle);
  
  const advCards = document.querySelectorAll('.adv-card');
  if (advCards[0]) {
    advCards[0].querySelector('h3').textContent = t.advantages.adv1.title;
    advCards[0].querySelector('p').textContent = t.advantages.adv1.desc;
  }
  if (advCards[1]) {
    advCards[1].querySelector('h3').textContent = t.advantages.adv2.title;
    advCards[1].querySelector('p').textContent = t.advantages.adv2.desc;
  }
  if (advCards[2]) {
    advCards[2].querySelector('h3').textContent = t.advantages.adv3.title;
    advCards[2].querySelector('p').textContent = t.advantages.adv3.desc;
  }
  if (advCards[3]) {
    advCards[3].querySelector('h3').textContent = t.advantages.adv4.title;
    advCards[3].querySelector('p').textContent = t.advantages.adv4.desc;
  }
  if (advCards[4]) {
    advCards[4].querySelector('h3').textContent = t.advantages.adv5.title;
    advCards[4].querySelector('p').textContent = t.advantages.adv5.desc;
  }
  if (advCards[5]) {
    advCards[5].querySelector('h3').textContent = t.advantages.adv6.title;
    advCards[5].querySelector('p').textContent = t.advantages.adv6.desc;
  }

  // About
  const aboutH2 = document.querySelector('.about-text h2');
  if (aboutH2) {
    const lines = aboutH2.querySelectorAll('br');
    if (lines.length > 0) {
      aboutH2.childNodes[0].textContent = t.about.title1 + '\n';
      aboutH2.childNodes[2].textContent = t.about.title2;
    }
  }
  setText('.about-text p:nth-child(3)', t.about.p1);
  setText('.about-text p:nth-child(4)', t.about.p2);
  setText('.about-text p:nth-child(5)', t.about.p3);

  const aboutHls = document.querySelectorAll('.about-hl-text');
  if (aboutHls[0]) {
    aboutHls[0].querySelector('h4').textContent = t.about.hl1.title;
    aboutHls[0].querySelector('p').textContent = t.about.hl1.desc;
  }
  if (aboutHls[1]) {
    aboutHls[1].querySelector('h4').textContent = t.about.hl2.title;
    aboutHls[1].querySelector('p').textContent = t.about.hl2.desc;
  }
  if (aboutHls[2]) {
    aboutHls[2].querySelector('h4').textContent = t.about.hl3.title;
    aboutHls[2].querySelector('p').textContent = t.about.hl3.desc;
  }
  if (aboutHls[3]) {
    aboutHls[3].querySelector('h4').textContent = t.about.hl4.title;
    aboutHls[3].querySelector('p').textContent = t.about.hl4.desc;
  }

  // Contact
  setText('.contact-info .section-label', t.contact.label);
  setText('.contact-info h3', t.contact.title);
  setText('.contact-info > p', t.contact.subtitle);

  const contactItems = document.querySelectorAll('.contact-item-text');
  if (contactItems[0]) {
    contactItems[0].querySelector('h4').textContent = t.contact.location;
    contactItems[0].querySelector('p').textContent = t.contact.locationText;
  }
  if (contactItems[1]) {
    contactItems[1].querySelector('h4').textContent = t.contact.email;
    // Email 不从翻译来，从 data.js 读取
  }
  if (contactItems[2]) {
    contactItems[2].querySelector('h4').textContent = t.contact.whatsapp;
  }
  if (contactItems[3]) {
    contactItems[3].querySelector('h4').textContent = t.contact.responseTime;
    contactItems[3].querySelector('p').textContent = t.contact.responseText;
  }

  // Contact form
  setText('.contact-form-wrap h3', t.contact.formTitle);
  const formGroups = document.querySelectorAll('.form-group label');
  if (formGroups[0]) formGroups[0].textContent = t.contact.name;
  if (formGroups[1]) formGroups[1].textContent = t.contact.company;
  if (formGroups[2]) formGroups[2].textContent = t.contact.emailLabel;
  if (formGroups[3]) formGroups[3].textContent = t.contact.phone;
  if (formGroups[4]) formGroups[4].textContent = t.contact.product;
  if (formGroups[5]) formGroups[5].textContent = t.contact.message;

  setPlaceholder('input[name="name"]', t.contact.namePlaceholder);
  setPlaceholder('input[name="company"]', t.contact.companyPlaceholder);
  setPlaceholder('input[name="email"]', t.contact.emailPlaceholder);
  setPlaceholder('input[name="phone"]', t.contact.phonePlaceholder);
  setPlaceholder('textarea[name="message"]', t.contact.messagePlaceholder);

  const submitBtn = document.querySelector('.form-submit');
  if (submitBtn) submitBtn.textContent = t.contact.submit;

  // Footer
  setText('.footer-brand p', t.footer.description);
  const footerCols = document.querySelectorAll('.footer-col h4');
  if (footerCols[0]) footerCols[0].textContent = t.footer.products;
  if (footerCols[1]) footerCols[1].textContent = t.footer.company;
  if (footerCols[2]) footerCols[2].textContent = t.footer.connect;

  const footerBottom = document.querySelector('.footer-bottom span');
  if (footerBottom) {
    footerBottom.innerHTML = `&copy; 2026 Ningbo Waterbear Environmental Technology Co., Ltd. ${t.footer.rights}.`;
  }

  // WhatsApp tooltip
  setText('.wa-float-tooltip', t.waTooltip);

  // 更新语言按钮状态
  document.querySelectorAll('.lang-btn').forEach(btn => {
    btn.classList.toggle('active', btn.dataset.lang === lang);
  });

  // 保存语言偏好
  localStorage.setItem('waterbear-lang', lang);

  // 重新渲染产品（更新"Inquire"按钮文本）
  if (typeof renderProducts === 'function') renderProducts();
  if (typeof renderCategories === 'function') renderCategories();
}

// ==================== 辅助函数 ====================
function setText(selector, text) {
  const el = document.querySelector(selector);
  if (el) el.textContent = text;
}

function setPlaceholder(selector, text) {
  const el = document.querySelector(selector);
  if (el) el.placeholder = text;
}

// ==================== 初始化 ====================
function initLanguage() {
  const saved = localStorage.getItem('waterbear-lang');
  if (saved && translations[saved]) {
    switchLanguage(saved);
  } else {
    // 默认英文
    switchLanguage('en');
  }
}

// 页面加载后初始化
if (document.readyState === 'loading') {
  document.addEventListener('DOMContentLoaded', initLanguage);
} else {
  initLanguage();
}

// 导出供全局使用
window.switchLanguage = switchLanguage;
window.getCurrentLang = () => currentLang;